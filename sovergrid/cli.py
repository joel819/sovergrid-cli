@cli.command()
@click.option(
    "--config", "-c",
    default=None,
    help="Path to sovergrid.yaml (defaults to current directory).",
)
def deploy(config):
    """Deploy your application to the decentralized network."""
    log.info(
        f"{Colors.BOLD}SoverGrid CLI v{__version__}{Colors.RESET}"
    )
    log.info("Initializing deployment pipeline...\n")

    # Load and validate config
    cfg = SoverGridConfig.load(config)

    # ── Pre-flight Dependency Scan ───────────────────────────────────────────
    # Scan the project for known SDKs and warn about missing env vars
    # BEFORE touching the blockchain or paying anything.
    log.info(f"  {Colors.BOLD}Running pre-flight dependency scan...{Colors.RESET}")
    detected = scan_project(os.getcwd())
    missing = check_env_vars_set(detected, existing_env=cfg.env_vars)
    print_scan_report(detected, missing)

    if missing:
        log.info(
            f"  {Colors.YELLOW}You have {len(missing)} missing variable(s).{Colors.RESET}\n"
            f"  Set them now (free) before deployment:\n"
            f"  {Colors.CYAN}sovergrid env set KEY=value{Colors.RESET}\n\n"
            f"  Or add them to the 'env:' block in your sovergrid.yaml and re-run.\n"
        )
        if not click.confirm("  Continue deployment anyway?", default=False):
            log.info("Deployment cancelled. Set your env vars and try again.")
            return

    # ── Anti-Fraud: Service Type Enforcement ─────────────────────────────────────
    # Cross-check the declared service types in sovergrid.yaml against what packages
    # were actually detected. GPU training packages CANNOT be deployed under 'compute'
    # to avoid bypassing the pay-per-use billing model.
    declared_services = list(cfg.service_types) if hasattr(cfg, "service_types") and cfg.service_types else [cfg.service_type]
    violations = check_service_type_fraud(declared_services, detected)

    if violations:
        log.error(f"\n  {Colors.RED}{Colors.BOLD}DEPLOYMENT BLOCKED: Service type mismatch detected.{Colors.RESET}")
        for v in violations:
            log.error(f"\n  {Colors.RED}{v['message']}{Colors.RESET}")
        log.error(
            f"\n  {Colors.YELLOW}This check exists to ensure fair billing.{Colors.RESET}\n"
            f"  GPU workloads are billed at $0.80/GPU hour, not a flat subscription rate.\n"
            f"  Contact support@sovergrid.network if you believe this is a false positive.\n"
        )
        return
    # ────────────────────────────────────────────────────────────────────────
    # ── Pricing Approval ────────────────────────────────────────────────────
    
    # Generate pricing summary based on requested services
    services_to_bill = list(declared_services)
    if "compute" not in services_to_bill:
        services_to_bill.insert(0, "compute")
        
    pricing_text = display_pricing_summary(services_to_bill)
    
    log.info(f"\n{Colors.CYAN}{Colors.BOLD}Deployment Cost Summary:{Colors.RESET}")
    print(pricing_text)
    
    if not click.confirm(f"\n  {Colors.BOLD}Approve these charges and deploy?{Colors.RESET}", default=True):
        log.info(f"\n{Colors.YELLOW}Deployment cancelled by user.{Colors.RESET}")
        return

    # ────────────────────────────────────────────────────────────────────────

    # Run the async deployment pipeline
    result = asyncio.run(run_deployment(cfg))

    if result.get("compute"):
        endpoint = result["compute"].get("endpoint", "N/A")
        deploy_id = result["compute"].get("deployment_id", cfg.app_name)

        log.info(
            f"\n  {Colors.BOLD}Your app is live at:{Colors.RESET} "
            f"{Colors.CYAN}{endpoint}{Colors.RESET}\n"
        )

        # ── Register subscription (auto-called after every deploy) ────────────
        # Reads wallet address from the local keystore / credentials file.
        # In DEMO MODE the backend records the subscription without charging.
        try:
            creds = _load_credentials()
            wallet = creds.get("wallet_address", "") if creds else ""

            if wallet:
                api_url  = _get_api_url()
                token    = creds.get("token", "")
                sub_resp = httpx.post(
                    f"{api_url}/api/subscriptions/create",
                    json={
                        "wallet_address": wallet,
                        "app_name":       cfg.app_name,
                        "deployment_id":  deploy_id,
                        "monthly_amount": 10.00,
                    },
                    headers={"Authorization": f"Bearer {token}"},
                    timeout=10,
                )
                if sub_resp.status_code == 200:
                    sub = sub_resp.json()
                    log.info(
                        f"  {Colors.BOLD}Subscription active.{Colors.RESET}\n"
                        f"  Monthly compute fee : {Colors.GREEN}$10.00 USDC{Colors.RESET}\n"
                        f"  Next billing date   : {Colors.CYAN}"
                        f"{sub.get('next_billing_date', 'N/A')[:10]}{Colors.RESET}\n"
                        f"  Manage             : {Colors.CYAN}sovergrid subscription status{Colors.RESET}\n"
                    )
        except Exception:
            # Non-fatal — deployment succeeded even if subscription API is unreachable
            log.warning("  Could not register subscription. Run 'sovergrid subscription status' later.")


@cli.command()
@click.option(
    "--port", "-p",
    default=8080,
    help="Port your application listens on (default: 8080).",
)
def init(port):
    """Initialize a new SoverGrid project in the current directory."""
    cwd = os.getcwd()
    project_name = os.path.basename(cwd)

    log.info(
        f"{Colors.BOLD}SoverGrid CLI v{__version__}{Colors.RESET}"
    )
    log.info(f"Initializing project '{project_name}'...\n")

    # Step 1: Generate sovergrid.yaml
    config_path = os.path.join(cwd, CONFIG_FILENAME)

    if os.path.exists(config_path):
        log.warning(
            f"{CONFIG_FILENAME} already exists. Skipping config generation."
        )
    else:
        config_content = DEFAULT_CONFIG.format(app_name=project_name)
        with open(config_path, "w") as f:
            f.write(config_content)
        log.info(
            f"{Colors.GREEN}Created {CONFIG_FILENAME}{Colors.RESET}"
        )

    # Step 2: Auto-detect project and generate Dockerfile
    project_type = detect_project_type(cwd)
    if project_type:
        generate_dockerfile(cwd, port=port)
    else:
        log.info(
            "No requirements.txt or package.json found.\n"
            "  A Dockerfile will be generated when you add one\n"
            "  and run 'sovergrid init' again."
        )

    # Step 3: Dependency scan — surface required env vars from the project
    detected = scan_project(cwd)
    missing = check_env_vars_set(detected)
    if detected:
        print_scan_report(detected, missing)
        if missing:
            log.info(
                f"  {Colors.YELLOW}Add the missing variables above to the 'env:' block\n"
                f"  in your sovergrid.yaml before you deploy.{Colors.RESET}\n"
            )

    # Step 4: Summary
    log.info(
        f"\n  {Colors.BOLD}{Colors.GREEN}"
        f"  Project initialized successfully.{Colors.RESET}\n\n"
        f"  Next steps:\n"
        f"  1. Review your {CONFIG_FILENAME}\n"
        f"  2. Set env vars: {Colors.CYAN}sovergrid env set KEY=value{Colors.RESET}\n"
        f"  3. Run {Colors.CYAN}sovergrid dev{Colors.RESET} to test locally\n"
        f"  4. Run {Colors.CYAN}sovergrid deploy{Colors.RESET} "
        f"to deploy\n"
    )


