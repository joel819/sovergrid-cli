@cli.command()
@click.option(
    "--config", "-c",
    default=None,
    help="Path to sovergrid.yaml (defaults to current directory).",
)
def deploy(config):
    """Deploy your application to the decentralized network."""
    log.info(
        f"{Colors.BOLD}SoverGrid CLI v{__version__}{Colors.RESET}"
    )
    log.info("Initializing deployment pipeline...\n")

    # Load and validate config
    cfg = SoverGridConfig.load(config)

    # Run the async deployment pipeline
    result = asyncio.run(run_deployment(cfg))

    log.info(
        f"\n  {Colors.BOLD}Your app is live at:{Colors.RESET} "
        f"{Colors.CYAN}{result['compute']['endpoint']}{Colors.RESET}\n"
    )


@cli.command()
@click.option(
    "--port", "-p",
    default=8080,
    help="Port your application listens on (default: 8080).",
)
def init(port):
    """Initialize a new SoverGrid project in the current directory."""
    cwd = os.getcwd()
    project_name = os.path.basename(cwd)

    log.info(
        f"{Colors.BOLD}SoverGrid CLI v{__version__}{Colors.RESET}"
    )
    log.info(f"Initializing project '{project_name}'...\n")

    # Step 1: Generate sovergrid.yaml
    config_path = os.path.join(cwd, CONFIG_FILENAME)

    if os.path.exists(config_path):
        log.warning(
            f"{CONFIG_FILENAME} already exists. Skipping config generation."
        )
    else:
        config_content = DEFAULT_CONFIG.format(app_name=project_name)
        with open(config_path, "w") as f:
            f.write(config_content)
        log.info(
            f"{Colors.GREEN}Created {CONFIG_FILENAME}{Colors.RESET}"
        )

    # Step 2: Auto-detect project and generate Dockerfile
    project_type = detect_project_type(cwd)
    if project_type:
        generate_dockerfile(cwd, port=port)
    else:
        log.info(
            "No requirements.txt or package.json found.\n"
            "  A Dockerfile will be generated when you add one\n"
            "  and run 'sovergrid init' again."
        )

    # Step 3: Summary
    log.info(
        f"\n  {Colors.BOLD}{Colors.GREEN}"
        f"  Project initialized successfully.{Colors.RESET}\n\n"
        f"  Next steps:\n"
        f"  1. Review your {CONFIG_FILENAME}\n"
        f"  2. Run {Colors.CYAN}sovergrid deploy{Colors.RESET} "
        f"to deploy\n"
    )


