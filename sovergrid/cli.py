    """Display information about SoverGrid and the current config."""
    log.info(
        f"\n"
        f"  {Colors.BOLD}{Colors.MAGENTA}SoverGrid{Colors.RESET} "
        f"v{__version__}\n"
        f"  The one-click, un-censorable decentralized AWS.\n\n"
        f"  Supported Compute:  Akash, Spheron, Golem\n"
        f"  Supported Storage:  Filecoin, Arweave, IPFS\n"
        f"  Payment Tokens:     USDC, SVR\n"
        f"  License:            MIT\n"
    )

    # Try to load config if it exists
    config_path = os.path.join(os.getcwd(), CONFIG_FILENAME)
    if os.path.exists(config_path):
        cfg = SoverGridConfig.load(config_path)
        log.info(f"  Current project config:{cfg.summary()}")
    else:
        log.info(
            f"  No {CONFIG_FILENAME} in current directory.\n"
            f"  Run {Colors.CYAN}sovergrid init{Colors.RESET} to get started.\n"
        )


